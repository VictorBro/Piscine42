int	ft_is_prime(int nb)
{
	int	x;

	if (nb < 2)
		return (0);
	if (nb == 2)
		return (1);
	if (nb % 2 == 0)
		return (0);
	x = 3;
	while (x * x <= nb)
	{
		if (nb % x == 0)
			return (0);
		x += 2;
	}
	return (1);
}
