#include <unistd.h>

void	ft_putnbr(int nb)
{
	char	ch;

	if (nb == -2147483648)
	{
		write(1, "-2147483648", 11);
		return ;
	}
	if (nb < 0)
	{
		write(1, "-", 1);
		nb *= -1;
	}
	if (nb < 10)
	{
		ch = '0' + nb;
		write(1, &ch, 1);
		return ;
	}
	ft_putnbr(nb / 10);
	ch = '0' + nb % 10;
	write(1, &ch, 1);
}
